import turtle
t=turtle.Turtle()


t.shape("turtle")
t.speed(50)

t.penup()
t.goto(-200,0)
t.pendown()

t.circle(100) #-200,0
t.penup()


t.forward(190) #-10,0
t.pendown()


t.circle(100)
t.penup()


t.forward(190) #180,0
t.pendown()


t.circle(100)
t.penup()


t.goto(-100,-120)
t.pendown()


t.circle(100)
t.penup()


t.forward(200)
t.pendown()


t.circle(100)
